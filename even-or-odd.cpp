#include<iostream>
using namespace std;
int main(){
    int n;
    cout<<"Enter the number :";
    cin>>n;
    if(n%2==0){
        cout<<n<<" is Even number";
    }
    else{
        cout<<n<<" is Odd number";
    }
}

// Output :
// Enter the number :6675437
// 6675437 is Odd number